             "ax.set_xticks(x); ax.set_xticklabels(names); ax.legend(); ax.set_title('M4 — NAS sweep against bracket')\n"
             "_plots = _PROJECTS / 'capstone' / 'milestone4' / 'plots'; _plots.mkdir(exist_ok=True)\n"
