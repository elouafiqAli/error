             "\n"
             "class GCN(torch.nn.Module):\n"
             "    def __init__(self, in_dim, hidden, out_dim):\n"
             "        super().__init__()\n"
             "        self.c1 = GCNConv(in_dim, hidden)\n"
             "        self.c2 = GCNConv(hidden, out_dim)\n"
             "    def forward(self, x, edge_index):\n"
             "        x = F.relu(self.c1(x, edge_index))\n"
             "        return self.c2(x, edge_index)\n"
             "\n"
             "data = _cora.to(device)\n"
             "model = GCN(data.num_features, 64, K).to(device)\n"
             "opt = torch.optim.Adam(model.parameters(), lr=0.01, weight_decay=5e-4)\n"
             "for epoch in range(200):\n"
             "    model.train(); opt.zero_grad()\n"
             "    out = model(data.x, data.edge_index)\n"
             "    loss = F.cross_entropy(out[data.train_mask], data.y[data.train_mask])\n"
             "    loss.backward(); opt.step()\n"
             "model.eval()\n"
             "with torch.no_grad():\n"
             "    logits = model(data.x, data.edge_index)\n"
